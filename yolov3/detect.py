class DetectorYolov3():
    def __init__(self):
        self.opt = parse_opt()
        self.device = "cuda" if torch.cuda.is_available() else "cpu"
        self.model =attempt_load(self.opt.weights, map_location=self.device, inplace=False, fuse=False)
        self.model.requires_grad_(True)
        self.model.to(self.device)
        self.model.eval()
    
    def detect(self,img):
        pred = self.model(img, augment=False, visualize=self.opt.visualize)[0]
        output,logits,obj_conf  = non_max_suppression(pred, self.opt.conf_thres, self.opt.iou_thres, self.opt.classes, self.opt.agnostic_nms, max_det=self.opt.max_det)
        return output,logits,obj_conf
        

